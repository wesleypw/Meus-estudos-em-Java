public class LixoSeco extends Lixo {
    private String material;
    private String coleta;
    private String coperativa;

    public void setMaterial(String material) {
        this.material = material;
    }
    public String getMaterial() {
        return material;
    }
    public void setColeta(String coleta) {
        this.coleta = coleta;
    }
    public String getColeta() {
        return coleta;
    }
    public void setCoperativa(String coperativa) {
        this.coperativa = coperativa;
    }
    public String getCoperativa() {
        return coperativa;
    }



    
}